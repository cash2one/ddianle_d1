#ifndef __CURRENCYTYPE_H__
#define __CURRENCYTYPE_H__

#define MAX_CURRENCY_TYPE  3

enum EMallCurrencyType
{
    EMallCurrencyType_None             = 0, // 不可用
    EMallCurrencyType_MCoin            = 1, // M币 ―― 人民币在游戏中的代币，唯一来源：人民币充值
    EMallCurrencyType_Money            = 2, // 金券 ―― 绑定在玩家身上的代币，除通过充值可能获得外，游戏内各种活动都可以获得，能购买商城中小部分物品
    EMallCurrencyType_BindMCoin        = 3, // 绑定M币 ―― 充值得到的奖励M币，绑定在玩家身上，能购买商城中大部分商品，但不能用于赠送给他人
    EMallCurrencyType_Max
};

#endif // __CURRENCYTYPE_H__

